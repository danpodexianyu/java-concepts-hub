package org.example.basics.comment;

public class MultipleLineComment {

    public static void main(String[] args) {
        // 多行注释

        /*
            如果我们将要注释的内容比较多，
            一行不能描述完整，
            除了使用多个单行注释，
            我们也可以使用多行注释
        */
        System.out.println("Hello World");
    }

}

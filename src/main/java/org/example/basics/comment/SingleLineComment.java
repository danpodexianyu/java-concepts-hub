package org.example.basics.comment;

public class SingleLineComment {

    public static void main(String[] args) {
        // 这是一个单行注释
        // 当然，如果一行注释写不下所有的注释，也可以换行
        // 使用多个单行注释完整描述注释的内容

        // 注释不会参与编译与运行，只是对代码进行解释和说明

        // 用法
        // 1. 我们可以在代码后面添加注释
        // 2. 也可以在代码的上方添加注释

        int number = 9; // 定义一个值为9的数字类型变量

        // 打印上面定义的数字类型变量
        System.out.println(number);
    }

}
